package com.six.hrpms.pojo;

public class ChangePassWord_po {
    private String oldPassword;
    private String newPassword;
    private String newCheckPassword;

    public String getOldPassword() {
        return oldPassword;
    }

    public void setOldPassword(String oldPassword) {
        this.oldPassword = oldPassword;
    }

    public String getNewPassword() {
        return newPassword;
    }

    public void setNewPassword(String newPassword) {
        this.newPassword = newPassword;
    }

    public String getNewCheckPassword() {
        return newCheckPassword;
    }

    public void setNewCheckPassword(String newCheckPassword) {
        this.newCheckPassword = newCheckPassword;
    }



}
